public class Animal {
    //Properties
    int legNumber;
    String colour;
    Boolean isMammal;

    //Constructors
    public Animal() {
    }

    public Animal(int legNumber, String colour, Boolean isMammal) {
        this.legNumber = legNumber;
        this.colour = colour;
        this.isMammal = isMammal;
    }

    //Methods
}
